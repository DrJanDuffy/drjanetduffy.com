<p><strong>Middle Schools</strong>: Clifford J Lawrence Junior High School, Walter Johnson Junior High School, Summit School, Kenny C Guinn Middle School</p>
<p><strong>High Schools</strong>: Spring Valley High School, Bonanza High School</p>
