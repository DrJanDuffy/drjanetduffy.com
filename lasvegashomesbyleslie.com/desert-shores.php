<p>Located at 7550 West Washington is the Summerlin Gateway Shopping Center featuring 20 business locations including Lowe’s, Pure Health Foods, Trader Joe’s, Christopher Ellis Massage, Sprint Wireless, U Swirl Frozen Yogurt and more.</p>
