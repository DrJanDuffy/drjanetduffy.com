<p>As a child in Lebanon, Antoine learned to love Mediterranean cuisine while watching his grandmother cook. With a love for cooking from an early age, Antoine was an accomplished chef by the time he entered the military in the late 1940’s. Based on the delicious street food he ate as a child, the couple opened their first restaurant in Las Vegas in 2006. Their newest Henderson location opened just a few years later.</p>
