<h2 id="below">Summerlin Master Planned Community</h2>
<p style="text-align: center;"><img title="Summerlin master planned community, Las Vegas, NV" src="http://www.lasvegashomesbyleslie.com/images/summerlin_2000.jpg" alt="Summerlin master planned community, Las Vegas, NV" width="1000" height="500" /></p>
<p>Summerlin is the consummate <a title="Las Vegas homes" href="/las-vegas-homes.php"><strong>master-planned community in Las Vegas</strong></a>, consisting of approximately 40,000 homes and over 120,000 people living within its borders. Real estate prices in Summerlin range from moderately priced starter homes and condos to luxury custom built home developments with multi-million dollar price tags. Brand-new and resale homes and urban condos are also available.</p>
<p>Developed in 1989 by The Howard Hughes Corporation, Summerlin is home to 20 villages, each with their own distinctive flair, with a projected 11 more still to come. There are nine golf courses, almost two dozen schools, churches, cultural meeting places, over 250 parks, the Summerlin Trail System (over 150 miles), business parks, shopping centers, an expansive Downtown retail district and medical facilities.</p>
