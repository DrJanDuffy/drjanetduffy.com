<h2 id="below">Solera Community Living</h2>
<p>Situated next to <strong><a title="Sun City Anthem" href="/sun-city-anthem.php">Anthem Sun City</a></strong>, in the master-planned community of <strong><a title="Anthem Homes" href="/anthem.php">Anthem</a></strong>, Solera features a much more peaceful setting. Real estate in Solera offers floor plans that are open and spacious, yet offer an intimate and cozy feel. Many homes boast breathtaking views of the glittering valley.</p>
<p>The Solera community is located in the <strong><a href="/home-listings-89052-zipcode.php">89052 zip code</a></strong>, in <a title="Henderson homes" href="/henderson.php"><strong>Henderson NV</strong></a>, and sits approximately 1,000 feet above the valley, often receiving a slight break from the summertime desert heat.</p>
<h2>Solera Amenities</h2>
<h3>Recreation</h3>
<p>Solera offers its residents a lifestyle of enjoyment and a wide variety of amenities. The community has a spacious clubhouse, fitness center, swimming pool, sports courts, billiards space, and number of game rooms for active residents. Social clubs are plentiful and include bunco, bridge, hiking clubs, tennis matches, and a literary guild.</p>
<p>There is always something happening on the social calendar and residents may or may not take part in the activities they find enjoyable. Although Solera does not have its own golf course, access to the nearby courses at Lexington and Concord is convenient. Residents of Solera golf at the Revere Golf Club in Sun City Anthem, or the <strong><a title="Anthem Country Club homes for sale" href="/anthem-country-club-homes.php">Anthem Country Club</a></strong>. Walking trails and sidewalks are located throughout the community and offer ample biking, strolling, or jogging opportunities.</p>
<h3>Dining</h3>
<p>Solera offers a diverse assortment of dining options sure to please even the most discriminate diners. Couples and families alike will love the diversity of offerings and price. Whether it’s a special anniversary dinner or a quick grab from the local drive-thru, Solera residents will find the right spot.</p>
<p>Area favorites include Elephant Bar Restaurant, Black Mountain Grill, P.F. Chang’s Chinese Bistro, Buffalo Wild Wings, Five Guys Burgers and Fries, Rachel’s Kitchen, and Tuscany Grill</p>
<p>Located at 11105 S. Eastern Avenue in Henderson, in the Mountain Dream Shopping Center, Tuscany Grill features some of the area’s best Italian, steaks and seafood. Boasting over 50 menu items, Tuscany Grill has been serving customers for over 13 years. In an era of fly-by-night establishments, Tuscany Grill has maintained its presence based on their core philosophies of fresh quality ingredients, consistency, and old-school customer service. In addition to customer favorites such as linguini almalfi, shrimp fradiavlo, and filet mignon, Tuscany Grill offers great gaming and complimentary drinks.</p>
<h3>Shopping</h3>
<p>Solera will find a number of shops and services within the immediate vicinity. Solera residents never have to venture far no matter what they need or want. With a mix of national retailers and local shops, there is something for everyone and every budget. Popular retailers include Target, Vons, Smith’s and Trader Joes. In addition, the award winning Galleria at Sunset shopping center is home to world-class dining, shopping and entertainment, and serves as the retail heartbeat of the area. Some amenities at Galleria include free Wi-Fi, package carrying assistance, and the Sunset Express trackless train.</p>
<h3>Health Care</h3>
<p>Living in Henderson away from the bright lights of Las Vegas doesn’t mean residents have to go without quality health care. Henderson is home to some of the area’s most dedicated health care professionals ready to serve at a moment’s notice. Area hospitals and clinics provide comprehensive or specialty care for people of all ages. Locations include Saint Rose Dominican Hospital – Siena Campus, Seven Hills Surgery Center, and Women’s Specialty Care, LLLP.</p>
<h2>Solera Schools</h2>
