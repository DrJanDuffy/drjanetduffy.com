<h3><em>Solera Public Schools</em></h3>
<p><strong>Elementary Schools</strong>: Frank S Lamping Elementary School, Elise L Wolff Elementary School</p>
<p><strong>Middle Schools</strong>: Del E Webb Middle School, Bob Miller Middle School</p>
<p><strong>High Schools</strong>: Coronado High School</p>
