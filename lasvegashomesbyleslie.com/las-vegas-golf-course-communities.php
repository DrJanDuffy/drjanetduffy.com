<p>Red Rock is a upscale community located within the larger master planned community of Summerlin. Red Rock real estate is situated among 740-acres and offers two Arnold Palmer designed 18-hole courses. In addition to the two courses, Red Rock features a 44,000 square foot clubhouse. <strong><a title="View Red Rock Country Club Listings" href="/red-rock-country-club.php">View Red Rock Country Club Listings</a></strong></p>
