<h3>Lake Las Vegas: South Shore Golf &amp; Country Club</h3>
<p>Lake Las Vegas South Shore is considered by many to be the finest golf course community in the Las Vegas area. South Shore has it all – including a picturesque location within a 2,200+ acre private canyon. At the center of the community is a gorgeous 300+ acre lake that serves as the focal point of the neighborhood. South Shore features the first Jack Nicholas Signature designed course in Nevada, and is surrounded by eye-popping vistas, Mediterranean homes, luxury hotels, and upscale shops. South Shore also features a private yacht club and marina. <strong><a title="View Lake Las Vegas South Shore Club Listings" href="/lake-las-vegas-south-shore-homes.php">View Lake Las Vegas South Shore Club Listings</a></strong></p>
<h3>Lake Las Vegas: Reflection Bay</h3>
<p>A Jack Nicklaus Signature Design Golf Course, situated on the north shore, Reflection Bay Golf Club been ranked among America's 100 Greatest Public Courses by Golf Digest. Utilizing the gorgeous waterfront setting, five holes play along 1.5 miles of shoreline. Other greens are tucked into the hillside behind the Hyatt Regency Resort. Built in 1998, the course is a Par 72, 7,261 yards, with six tee positions. Annual memberships are available.</p>
